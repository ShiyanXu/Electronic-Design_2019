//IO模拟SPI时序
void OLED_WR_Byte(uchar dat,uchar cmd)
{	
	uchar i;
	if(cmd)
		DC_H;
	else 
		DC_L;

	for(i=0;i<8;i++)
	{			  
		SCL_L;

		if(dat&0x80)
		   SDA_H;
		else
		   SDA_L;

		SCL_H;

		dat<<=1;   
	}				 		  
	
	DC_H;
} 

